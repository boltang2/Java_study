package ex1_multi_array;

public class Ex1_multi_array {
	public static void main(String[] args) {
		
		// 다차원 배열
		// 1차원 배열이 2개모이면 2차원 배열
		int[][] test = new int[2][3];
		
		test[0][0] = 10;
		test[0][1] = 20;
		test[0][2] = 30;
		test[1][0] = 40;
		test[1][1] = 50;
		test[1][2] = 60;
	}
}
